namespace FuncionesCore
{
    public class FUnidades
    {
        public static double HoraAMinuto(double pTiempo)
        {
            return pTiempo * 60;
        }

        public static double HoraASegundo(double pTiempo)
        {
            return pTiempo * 3600;
        }

        public static double KilometroAMetro(double pDistancia)
        {
            return pDistancia * 1000;
        }

        public static double MetroAKilometro(double pDistancia)
        {
            return pDistancia / 1000;
        }

        public static double MinutoASegundo(double pTiempo)
        {
            return pTiempo * 60;
        }

        public static double MinutoAHora(double pTiempo)
        {
            return pTiempo / 60;
        }

        public static double SegundoAHora(double pTiempo)
        {
            return pTiempo / 3600;
        }

        public static double SegundoAMinuto(double pTiempo)
        {
            return pTiempo / 60;
        }

        public static double RadianesAGrados(double pRad)
        {
            return pRad * 57.2958;
        }

        public static double GradosARadianes(double pGrados)
        {
            return pGrados * 0.0174533;
        }
    }
}